    <h3>About Wayriad - </h3>
    <p>
    	Wayriad is a web tool that promotes open-source spirit in disciplines other than computer science. 
    	Even users without specialized skills can contribute a lot to the community, 
    	as long as they are <strong>careful observers of life</strong>.
    	<br>
    	<br>
    	With simple steps to set up an account, users can easily post and discuss a project in either literature, engineering, or other field.
    	 You can post an idea of invention that you do not have ability to carry out, and let other users with professional knowledge to revise and eventually realize this plan.
    	<br>
    	<br>
    	There are several modes of discussion available (some are still under construction),including a featured discussion board with multiple stages
    	 - an innovation of traditional forum with a combined feature of Q$A sites. This type of design enables users to discuss on an issue and make clear progress, especially complex and multifaceted proposals.
    	 
